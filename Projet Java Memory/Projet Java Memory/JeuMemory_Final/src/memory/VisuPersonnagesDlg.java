    public  void initPanneau()
    {   
        LesPersonnages lc = j.getPaquet();
        int nbi= lc.getTaille();
        // taille de la grille nxn
        int n= (int) Math.floor(Math.sqrt(nbi))+1; 
        Panneau.setLayout(new java.awt.GridLayout(n, n));
        for (int i =0; i<nbi; i++)
        { 
            JButton bt= new JButton();
            Panneau.add(bt);
        }
    }

